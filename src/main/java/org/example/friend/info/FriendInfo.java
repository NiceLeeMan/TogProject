package org.example.friend.info;

public class FriendInfo {

	private String userId;
	private String name;
	private String profileImgUrl;
	
	public FriendInfo() {}
	
	public String getId() {
		return userId;
	}
	
	public String getName() {
		return name;
	}
	
	public String getProfileImgUrl() {
		return profileImgUrl;
	}
	
}
